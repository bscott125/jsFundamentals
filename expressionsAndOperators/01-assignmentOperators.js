/*
Assignment Operators
*/

let r = 5//our equal sign is the assignment

//Addition Assignment
r += 1; // r = r + 1 or, 5 + 1 in this case
console.log(r);

//SUBTRACTION ASSIGNMENT
r -= 2; // r = r - 2
console.log(r);

//MULTIPLICATION ASSIGNMENT
r *= 2;// r = r * 2
console.log(r)

//DIVISION ASSIGNMENT
r /= 1;// r = r / 1
console.log(r);

//EXPONENTIAL ASSIGNMENT
r **= 2; // r = r * r   it has if to the power
console.log(r);

//REMAINDER (modulus) ASSIGNMENT
r %= 1; // r = r % 1
console.log(r);
